<h2>Bienvenue sur le site de gestion des services de vos communes !</h2>




<p id="paragDescription">

Vous pourrez y consulter les statistiques de vos services communaux, gérer les services disponibles ainsi que les communes qui vous sont rattachées et administrer vous même les périodes d'essais de vos différents services.

<br>

Retrouvez directement toutes ces fonctionnalités dans le menu juste à gauche.

</p>




<p>Plus de détails sur chacune des fonctionnalités disponible :</p>

<ol>

<li><strong>Statistiques</strong></li>

<p>Dans cette section, vous aurez la possibilité de générer des statistiques comme : 

<ul>

<li>Nombre d’instances pour 3 tables de votre choix ;</li></br>

<li>Liste de chaque enfant et de son école actuelle ;</li></br>

<li>Liste des enfants avec le nom de la cantine où ils mangeront le 01/01/2024 ;</li></br>

<li>Paires d’enfants ayant les mêmes nom et prénom, mais inscrits dans des écoles différentes ;</li></br>

<li>Top-3 des départements ayant le plus de communes ;</li></br>

<li>Top-3 des services les plus demandés (par les citoyen·ne·s) ;</li></br>

<li>Top-3 des services les plus proposés (par les communes) ;</li></br>

<li>Top-3 des communes qui réalisent le plus d’unions.</li></br>

</ul>

</p>

<li><strong>Ajouter un service</strong></li>

<p>Ici, vous serez en mesure d'ajouter de nouveaux services en plus de ceux déjà disponibles en remplissant le formulaire demandé. Vous pourrez affecter ces nouveaux services
aux communes de votre choix.</p> </br>

<li><strong>Intégrer une commune</strong></li>

<p>La fonctionnalité d'intégration des communes vous permet de migrer les données des communes de certaines région.</p></br>

<li><strong>Générer des périodes d'essai</strong></li>

<p>Afin de faire découvrir nos fonctionnalités nous vous proposons des périodes d'essai pour la gestion de vos services. Grace à cela, vous pouver affecter de nouveaux services
à vos communes pendant un temps donné afin qu'elle expérimente la gestion de ce service.</br>

Dans cette section vous gérez vous même les périodes d'essai des services de vos communes.</p></br>

</ol>



